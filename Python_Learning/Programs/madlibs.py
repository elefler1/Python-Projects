# Madlibs Game
# Word game where you creater a story by filling in the blanks with words of your choice.

adjective1 = input("Enter an adjective (descriptive): ")
noun = input("Enter a noun (person, place, or thing): ")
adjective2 = input("Enter another adjective (descriptive): ")
verb1 = input("Enter a verb ending with 'ing' (action word): ")
adjective3 = input("Enter one more adjective (descriptive): ")

print("Welcome to the Madlibs Game!")
print(f"Today I went to a {adjective1} zoo.")
print(f"In an exhibit, I saw a {noun}")
print(f"{noun} was {adjective2} and {verb1}")
print(f"I was {adjective3}!")