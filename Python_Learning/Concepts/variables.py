# Variables = A container for a value (e.g., an integer, string, float, or boolean)
#             A variable behaves as if it was the value it contains.

# Strings = A sequence of characters, enclosed in single or double quotes.
first_name = "Eric"
food = "pizza"
email = "elefler07@gmail.com"

print(f"Hello, {first_name}, your favorite food is {food} and your email is {email}.")

# Integers = Whole numbers, both positive and negative, without decimals.
age = 35
quantity = 3
num_of_stundents = 20

print(f"You are {age} years old.")
print(f"You have {quantity} pizzas.")
print(f"There are {num_of_stundents} students in the class.")

# Floats = Numbers with decimal points, representing real numbers.
price = 10.99
gpa = 3.75
distance = 5.5

print(f"The price of the pizza is ${price}.")
print(f"Your GPA is {gpa}.")
print(f"You have {distance}km to go.")

# Booleans = Represents truth values, either True or False.
is_student = True
for_sale = False
is_online = True

print(f"Is the person a student?: {is_student}")
print(f"Is the item for sale?: {for_sale}")
print(f"Is the person online?: {is_online}")
